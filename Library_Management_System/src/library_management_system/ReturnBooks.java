    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed

        try {
            String returnstatus = "Returnd";

            //returndate();
            connect = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "nh123456");
            pstInsert = connect.prepareStatement("insert into returnbooks(bookid,studentid,issuedate,possiblereturndate,returndate,returnstatus)values(?,?,?,?,?,?)");
            pstInsert.setInt(1, Integer.parseInt(txbookid.getText()));
            pstInsert.setInt(2, Integer.parseInt(txstudentid.getText()));
            pstInsert.setString(3, txissuedate.getText());
            pstInsert.setString(4, txpossiblereturntdate.getText());
            pstInsert.setString(5, txreturn.getText());
            pstInsert.setString(6, returnstatus);
            int n = pstInsert.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Successfully Returnd");
            } else {
                JOptionPane.showMessageDialog(null, "Returnd not Successfully");
            }

            //----code for update booklist ----//
            pstupdatebooklist = connect.prepareStatement("update booklist set status=? where bookid=?");
            pstupdatebooklist.setString(1, "Available");
            pstupdatebooklist.setInt(2, Integer.parseInt(txbookid.getText()));
            pstupdatebooklist.executeUpdate();

            //----code for update student bookissuestatus ----//
            pstupdatestudent = connect.prepareStatement("update student set bookissuestatus=? where studentid=?");
            pstupdatestudent.setString(1, "NO");
            pstupdatestudent.setInt(2, Integer.parseInt(txstudentid.getText()));
            pstupdatestudent.executeUpdate();

            //----code for update issuebooks ----//
            pstupdateissuebook = connect.prepareStatement("update issuedbooks set returnbook=? where bookid=?");
            pstupdateissuebook.setString(1, "Returnd");
            pstupdateissuebook.setInt(2, Integer.parseInt(txbookid.getText()));
            pstupdateissuebook.executeUpdate();

        } catch (Exception e) {
            e.printStackTrace();

        }

    }//GEN-LAST:event_btnreturnActionPerformed

