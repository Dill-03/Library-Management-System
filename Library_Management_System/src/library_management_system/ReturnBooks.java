    private void txbookidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txbookidKeyReleased

        if (txbookid.getText().equals("")) {
            txbookid.setText(String.valueOf(0));
        }

        try {

            connect = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "nh123456");
            pst3 = connect.prepareStatement("select * from issuedbooks where bookid=?");
            pst3.setInt(1, Integer.parseInt(txbookid.getText()));
            rst = pst3.executeQuery();

            if (rst.next()) {
                txstudentid.setText(String.valueOf(rst.getInt(3)));
                txissuedate.setText(rst.getString(4));
                txpossiblereturntdate.setText(rst.getString(5));
                txreturn.setText(String.valueOf(LocalDate.now()));

            } else {
                JOptionPane.showMessageDialog(null, "The Book is not issued");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_txbookidKeyReleased

